				<h4>Révéler ou reformuler les objectifs</h4>
				<button class="js-show-step-details"><img src="app/img/icon-plus.svg" alt="voir plus"></button>
			</div>
			<div class="process__step__detail">
				<h5>Pourquoi ?</h5>
				<p>Parce que la demande ne correspond qu’à la première étape de la réflexion,
elle ne peut être ni précise ni exhaustive, ce qui est tout à fait normal. En outre, les documents peuvent comporter des lacunes, car ceux qui les rédigent croient certaines informations connues de tous, ce qui est évident pour le client ne l’est pas toujours pour l’équipe de designers. Enfin, ces documents souffrent parfois de certains travers dans la formulations du bu recherché et des objectifs du projet.</p>
				<h5>Concrètement</h5>
				<p>Cela consiste à réunir toutes les parties prenantes pour un entretien de 45 minutes et faire discuter tout le monde sur les objectifs du projet, Si il existe des discordances et des contradictions c’est le moment de les souligner et de demander un arbitrage. Un fois que toute le monde est d’accord un document est rédigé et valider par le client. Cet élément fondateur nous prémunit contre les erreurs d'interprétation et les non-dits.</p>
		</div>
		</li>

		<li class="process__step">		
			<div class="process__step__resume">
				<img src="app/img/methode/fluxi-methode-utilisateur.svg" class="process__icon" alt="">
