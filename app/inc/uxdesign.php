			<div class="process__step__detail">
				<h5>Pourquoi ?</h5>
				<p>Vous n’êtes pas l’utilisateur !<br>Telle est la profession de foi du design centré sur l’utilisateur. Rien n’est plus juste : on ne peut se contenter d’assomptions ou de présupposés dès lors qu’il s’agit d'êtres humains. La façon dont ils pensent, parlent ou agissent n’est pas la même pour tous. C’est pourquoi la première étape du processus consiste à identifier et comprendre l’utilisateur. Il existe de très nombreuses méthodes pour y parvenir.</p>
				<h5>Concrètement</h5>
				<p>L’entretien utilisateur est une étape fondamentale qui consiste à questionner pertinemment de potentiels utilisateurs pour comprendre leurs comportements usuels ainsi que leurs besoins par rapport au dispositif.<br>Quand le projet consiste à refondre ou à faire évoluer un dispositif (site, application mobile ou tablette, borne interactive...) il es possible d’en consulter les statistiques pour en déduire les comportement des utilisateurs et identifier les point à améliorer.</p>
		</div>
